#include "../minishell.h"

void	echo(char **argv)
{
	int	i;
	int	nl;

	i = 1;
	nl = 0;
	while (argv[i])
	{
		if (argv[i] == "-n")
		{
			nl = 1;
			i++;
		}
		else 
		{
			printf("%s", argv[i]);
			if (argv[i + 1])
				printf(" ");
			i++;
		}
	}
}
